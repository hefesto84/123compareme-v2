package es.ubiqua.compareme.model;

public class HotelCity {
	
	private int id;
	private int id_hotel;
	private int modelWidget;
	private int cssWidget;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getId_hotel() {
		return id_hotel;
	}
	
	public void setId_hotel(int id_hotel) {
		this.id_hotel = id_hotel;
	}
	
	public int getModelWidget() {
		return modelWidget;
	}
	
	public void setModelWidget(int modelWidget) {
		this.modelWidget = modelWidget;
	}
	
	public int getCssWidget() {
		return cssWidget;
	}
	
	public void setCssWidget(int cssWidget) {
		this.cssWidget = cssWidget;
	}
	
	
}
